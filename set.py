#Set
s={1,2,3,4,5}
print(s)

#Create set using multiple data types
s={10,20,5.7,5,9,"Sona",True}
print(s)

#Create set using duplicate values
s={10,20,5.7,5,9,10,20}
print(s)

#Create set using set()
s=set([1,2,3,4,5])
print(s)

s=set((1,2,3,4,5))
print(s)

#Adding Element
t={10,20,60,30,50}
t.add(100)
print(t)

#Adding Multiple Elements
t={10,20,60,30,50}
t.update((100,2,40,70))
print(t)





